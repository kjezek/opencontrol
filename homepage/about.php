<h2>O programu</h2>

<p>
    OpenControl je jednoduchý, přehledný program pro správu skladového 
    hospodářství nenáročný na obsluhu, instalaci a výpočetní techniku.
</p>

<h3>Co program umožňuje</h3>

<ul>
    <li>Počítačovou evidenci skladových zásob</li>
    <li>Přehled stavu skladu</li>
    <li>Přehled dodavatelů a odběratelů</li>
    <li>Tvorbu příjmových a výdajových dokladů</li>
    <li>Maloobchodní prodej</li>
    <li>Tisk dokladů a přehledů</li>
    <li>Inventuru skladu</li>
    <li>Měsíční a roční uzávěrky hospodaření</li>
    <li>Správu uživatelských účtů</li>
    <li>Provoz aplikace na jedné stanici i práci v síti</li>
</ul>

<h3>Program je určen pro menší firmy, zvažte proto také následující</h3>
<ul>
    <li>Nejedná se o účetní program</li>
    <li>Nejedná se o ERP systém obecné plánování zdrojů</li>
    <li>Nejedná se o CRM systém řešící vztahy se zákazníky</li>
</ul>

