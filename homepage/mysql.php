<h2>Instalace MySQL</h2>

<p>

Databázový systém MySQL je třeba pro využití aplikace v síti, kde 
data jsou přistupována z více stanic.
    
</p>

<p>
    Pro základní využití na jedné stanici (bez práci v síti) databázi neinstalujte.
</p>


<p>Zde naleznete základní informace o instalaci na MS Windows</p>

    Instalační soubory stáhněte na:
    <ul>
        <li><a href="http://downloads.mysql.com/archives/mysql-5.1/mysql-5.1.40-winx64.msi">Instalátor Windows 64b</a></li>
        <li><a href="http://downloads.mysql.com/archives/mysql-5.1/mysql-5.1.40-win32.msi">Instalátor Windows 32b</a></li>
    </ul>



<h2>Obrázky z instalace</h2>

Při instalaci zvolte nastavení dle následujících obrázků.

Při instalaci si dejte pozor zejména na:
<ul>
    <li>Umístění adresáře s daty, aby jste předešli jeho nechtěnému smazání
    <li>Heslo pro přístup k databázi, budete ho zadávat při startu programu    
</ul>

